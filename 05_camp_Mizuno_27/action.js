//Aboutについて
$(".t1").on("mouseover", function(){
    //   マウスを合わせたらされたら実行したいことを書く
    $(".t1").css('color','white');
    $(".t1").css('background-color','lightpink');
});
$(".t1").on("mouseout", function(){
    //   マウスを合わせたらされたら実行したいことを書く
    $(".t1").css('color','black');
    $(".t1").css('background-color','white');
});

